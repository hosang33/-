package kr.pren.vo;

import java.util.Date;

public class SaleItem {
	private int no;
	private int subCategoryNo;
	private String title;
	private String content;
	private String contentsGuide;
	private String contentsRefund;
	private int price;
	private int likes;
	private int star;
	private String state;
	private String logoName;
	private String isSolo;
	private Date uploadDate;
	private User user;
	private int cnt;
	
	public SaleItem() {}

	public int getNo() {
		return no;
	}

	public void setNo(int no) {
		this.no = no;
	}


	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(int price) {
		this.price = price;
	}

	public int getLikes() {
		return likes;
	}

	public void setLikes(int likes) {
		this.likes = likes;
	}

	public int getStar() {
		return star;
	}

	public void setStar(int star) {
		this.star = star;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public String getLogoName() {
		return logoName;
	}

	public void setLogoName(String logoName) {
		this.logoName = logoName;
	}

	

	public String getIsSolo() {
		return isSolo;
	}

	public void setIsSolo(String isSolo) {
		this.isSolo = isSolo;
	}

	public Date getUploadDate() {
		return uploadDate;
	}

	public void setUploadDate(Date uploadDate) {
		this.uploadDate = uploadDate;
	}

	public String getContentsGuide() {
		return contentsGuide;
	}

	public void setContentsGuide(String contentsGuide) {
		this.contentsGuide = contentsGuide;
	}

	public String getContentsRefund() {
		return contentsRefund;
	}

	public void setContentsRefund(String contentsRefund) {
		this.contentsRefund = contentsRefund;
	}

	public User getUser() {
		return user;
	}

	public void setUser(User user) {
		this.user = user;
	}

	public int getSubCategoryNo() {
		return subCategoryNo;
	}

	public void setSubCategoryNo(int subCategoryNo) {
		this.subCategoryNo = subCategoryNo;
	}

	public int getCnt() {
		return cnt;
	}

	public void setCnt(int cnt) {
		this.cnt = cnt;
	}

	
}
