package kr.pren.vo;

import java.util.Date;

public class Message {
	private int no;
	private int sendUserNo;
	private int ReceiveUserNo;
	private String content;
	private String star;
	private String isRead;
	private String sellState;
	private Date sendDate;
	
	public Message() {}
	
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public int getSendUserNo() {
		return sendUserNo;
	}
	public void setSendUserNo(int sendUserNo) {
		this.sendUserNo = sendUserNo;
	}
	public int getReceiveUserNo() {
		return ReceiveUserNo;
	}
	public void setReceiveUserNo(int receiveUserNo) {
		ReceiveUserNo = receiveUserNo;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	
	public String getStar() {
		return star;
	}

	public void setStar(String star) {
		this.star = star;
	}

	public String getIsRead() {
		return isRead;
	}

	public void setIsRead(String isRead) {
		this.isRead = isRead;
	}

	public String getSellState() {
		return sellState;
	}
	public void setSellState(String sellState) {
		this.sellState = sellState;
	}
	public Date getSendDate() {
		return sendDate;
	}
	public void setSendDate(Date sendDate) {
		this.sendDate = sendDate;
	}
	
	
}
